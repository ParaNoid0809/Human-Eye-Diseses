dme = '''**For DME (Diabetic Macular Edema)**:
- **Key Recommendations**:
    - **Endocrinology Consultation**: Alongside ophthalmic care, managing diabetes with an endocrinologist is vital since diabetes plays a major role in DME progression.
    - **Treatment Options**: 
      - **Anti-VEGF Injections**: Anti-VEGF therapy is the primary treatment to reduce retinal swelling and improve vision.
      - **Corticosteroid Implants**: For cases where anti-VEGF therapy is less effective, corticosteroid injections or implants may be considered.
      - **Laser Photocoagulation**: Focal or grid laser therapy can help treat areas of leakage, particularly in non-central DME.
    - **Managing Blood Sugar and Blood Pressure**: 
      - **Control Blood Sugar**: Keep HbA1c levels under 7% to slow the progression of diabetic retinopathy and macular edema.
      - **Blood Pressure Management**: Hypertension can worsen DME. Aim to keep your blood pressure below 140/80 mmHg.
    - **Monitoring Guidelines**:
      - **Frequent Eye Exams**: Regular OCT scans (every 3-6 months) are essential to monitor fluid accumulation and treatment effectiveness.
      - **Comprehensive Diabetes Management**: Ensure strict control of blood glucose and blood pressure levels to prevent further episodes of macular edema.
    - **Next Steps**: 
      - Schedule appointments with both your retina specialist and endocrinologist.
      - If not already started, initiate anti-VEGF treatment, and focus on managing your systemic health conditions.

  '''

drusen = '''**For Drusen (Early AMD)**:
- **Key Recommendations**:
    - **Dietary Adjustments**: 
      - **Antioxidant-Rich Foods**: Incorporate foods high in vitamins C and E, zinc, copper, and beta-carotene, which can help delay AMD progression. Foods like spinach, kale, and omega-3-rich fish are beneficial.
      - **Supplements**: Consider the **AREDS2** formulation, especially for patients at moderate to high risk of developing advanced AMD.
    - **Lifestyle Changes**: 
      - **Quit Smoking**: Smoking increases the risk of AMD progression, so quitting is strongly advised.
      - **UV Protection**: Protect your eyes by wearing UV-blocking sunglasses to minimize retinal damage from sunlight.
    - **Monitoring and Follow-Up**:
      - **OCT Scans**: For early AMD or drusen, schedule OCT scans every 6 to 12 months to track any structural changes.
      - **Amsler Grid Monitoring**: Regularly use an Amsler grid at home to check for changes like wavy lines or blank spots, which could indicate progression to wet AMD.
    - **Next Steps**:
      - Discuss AREDS2 supplements and dietary/lifestyle adjustments with your healthcare provider.
      - Regularly monitor your condition with OCT scans and be vigilant for any vision changes.

  '''

